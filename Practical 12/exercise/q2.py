#Write a Python program that will calculate area and circumference of circle using inbuilt Math Module
import math
pi=math.pi
radius=abs(int(input("enter the radius:")))
print("Area of circle:",pi*radius*radius)
print("Circumference of circle:",2*pi*radius)

